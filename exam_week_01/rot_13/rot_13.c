#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	if (argc == 2)
	{
		i = 0;
		while (argv[1][i])
		{
			if ((65 <= argv[1][i] && argv[1][i] <= 77)
				|| (97 <= argv[1][i] && argv[1][i] <= 109))
				argv[1][i] += 13;
			else if ((78 <= argv[1][i] && argv[1][i] <= 90)
				|| (110 <= argv[1][i] && argv[1][i] <= 122))
				argv[1][i] -= 13;
			write(1, &argv[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
